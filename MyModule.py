#!/usr/bin/env python

"""modules in python"""

def addNumbers(a, b):
    """This function adds two numbers"""
    total = a + b
    print(total)

def subtractNumbers(a, b):
    """This function subtracts two numbers"""
    total = a - b
    print(total)

def power(a,b):
    """This function returns the result of a **b """
    total = a ** b
    print(total)
